// everything in javascript happens inside an execution context

// first component of the execution context is the memory component
// the memory component stores the variables and functions as key value pairs
// memory component is also known as variable environment

// second component of the execution context is the code component
// in this component code is executed one line at a time
// code component is also known as the thread of execution

// javascript is a synchronous single threaded language

// javascript is single threaded means it can execute one command at a time

// javascript is synchronous means the next line will be executed only after the current line execution is done

// javascript isn't possible without this execution context





